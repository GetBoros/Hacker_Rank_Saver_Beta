#include "Messenger.h"

// AMsg
AMsg::AMsg(string msg)
    : Msg(msg)
{
}
//------------------------------------------------------------------------------------------------------------------
string AMsg::Get_Msg()
{
    return Msg;
}
//------------------------------------------------------------------------------------------------------------------




// ABrakets_Msg
ABrakets_Msg::ABrakets_Msg(string msg):AMsg(msg)
{
}
//------------------------------------------------------------------------------------------------------------------
string ABrakets_Msg::Get_Msg()
{
    string result;

    result = "[" + AMsg::Get_Msg() + "]";

    return result;
}
//------------------------------------------------------------------------------------------------------------------




// APrinter
APrinter::APrinter()
{
}
//------------------------------------------------------------------------------------------------------------------
void APrinter::Print(AMsg *msg)
{
    cout << msg->Get_Msg() << endl;
}
